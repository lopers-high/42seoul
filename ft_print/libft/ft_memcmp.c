#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{	
	size_t				i;
	const unsigned char	*str_s1;
	const unsigned char	*str_s2;

	i = 0;
	str_s1 = s1;
	str_s2 = s2;
	while (i < n)
	{
		if (str_s1[i] != str_s2[i])
			return (str_s1[i] - str_s2[i]);
		i++;
	}
	return (0);
}
